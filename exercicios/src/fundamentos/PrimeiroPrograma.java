public class PrimeiroPrograma {
	/**
	 * @param args
	 * @=opções de comentários Um código com muitos comentários, ficará poluído,
	 *          dificultando o entendimento
	 */

	public static void main(String[] args) {
		/* Main = Método Principal */
		System.out.println("Hello world!");
		/*
		 * System.out.println = Método de exibição de texto no console, pode ser
		 * abreviado com: syso+"ctrl+espaço"
		 * 
		 * ctrl+shift+f = Organizar o código de maneira identada
		 * 
		 * ctrl + / = Comenta/Descomenta uma linha
		 */
		System.out.println("Primeiro programa, Parte #1");
		System.out.println("Primeiro programa, Parte #2");
		System.out.println("Primeiro programa, Parte #3");
		System.out.println("Primeiro programa, Parte #4");
		/*
		 * CTRL+Alt, no início da linha, duplicaá para a linha debaixo
		 */
	}
}
